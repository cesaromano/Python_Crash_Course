#	ALMACENANDO TUS FUNCIONES EN MODULOS
#Una de las ventajas de las funciones es la forma de separar bloques desde tu 
#programa principal. Usando nombres descriptivos para tus funciones, tu programa 
#principal puede ser mas facil de seguir. Puedes ir mas alla almacenando tus
#funciones en un archivo separado llamado "modulo" y luego importando ese modulo
#en tu programa principal. La declaracion import le dice a Python que haga el
#codigo en un modulo disponible en el actual archivo de programa.
#Almacenando tus funciones en un archivo separado te permitira dejar los detalles
#del codigo de tu programa y enfocarte en su logica de alto nivel. Tambien te 
#permitira reusar funciones en muchos programas diferentes. Cuando almacenas tus
#funciones en archivos separados, puedes compartir esos archivos con otros
#programadores sin necesidad de compartir el programa entero. Saber como importar
#funciones tambien te permite usar liberias de funciones que otros programadores
#han escrito.
#Hay varias maneras de importar un modulo, y te explicare cada una de ellas brevemente.

