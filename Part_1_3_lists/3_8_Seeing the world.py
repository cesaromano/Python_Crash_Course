#Almacena 5 lugares del mundo que quieras visitar en una lista de manera aleatoria
lugares = ['japon', 'corea', 'rusia', 'egipto', 'santa cruz']
print(lugares) 
#Usa la funcion sorted() para imprimir en orden alfabetico sin modificar la lista actual
print(sorted(lugares))
#verifica que la lista conserve su orden original
print(lugares)
#Usa el metodo reverse para cambiar el orden de la lista 
lugares.reverse()
print(lugares)
#usa el metodo reverse() para recuperar el orden
lugares.reverse()
print(lugares)
#Usa el metodo sort() para cambiar el orden alfabeticamente
lugares.sort()
print(lugares)
#Usa el metodo sort() para cambiar el orden alfabeticamente pero contrario
lugares.sort(reverse=True)
print(lugares)